import { createContext, useContext, useState, useEffect, ReactNode } from "react";

export type Language = 'en' | 'th';

interface LanguageContextType {
  language: Language;
  setLanguage: (lang: Language) => void;
  t: (key: string, params?: Record<string, string | number>) => string;
}

const LanguageContext = createContext<LanguageContextType | undefined>(undefined);

interface LanguageProviderProps {
  children: ReactNode;
}

// Translation function
const t = (language: Language, key: string, params?: Record<string, string | number>): string => {
  const translations = getTranslations(language);
  let text = translations[key];
  
  // If translation doesn't exist, return the key
  if (text === undefined) {
    return key;
  }
  
  // If translation is empty string, return empty string (don't fall back to key)
  if (text === '') {
    return '';
  }
  
  if (params) {
    Object.entries(params).forEach(([param, value]) => {
      text = text.replace(new RegExp(`{{${param}}}`, 'g'), value.toString());
    });
  }
  
  return text;
};

// Import translation files
const getTranslations = (language: Language): Record<string, string> => {
  switch (language) {
    case 'en':
      return enTranslations;
    case 'th':
      return thTranslations;
    default:
      return enTranslations;
  }
};

